var express = require('express');
var path = require('path');
var favicon = require('serve-favicon');
var logger = require('morgan');
var cookieParser = require('cookie-parser');
var bodyParser = require('body-parser');
var fs = require('fs');

var routes = require('./routes/index');
var imageview = require('./routes/imageview');

var app = express();

app.set('views', path.join(__dirname, 'views'));
app.set('view engine', 'ejs');

app.use(logger('dev'));
app.use(bodyParser.json());
app.use(bodyParser.urlencoded({ extended: false }));
app.use(cookieParser());
app.use(express.static(path.join(__dirname, '/../../Book')));
app.use(express.static(__dirname + '/public'));
//fs.realpath(__dirname);


var htpasswd = path.join(__dirname, "user.htpasswd");
if (fs.existsSync(htpasswd)) {
  var auth = require('http-auth');
  var basic = auth.basic({
    realm: "Private Page",
    file: htpasswd
  });
  app.use(auth.connect(basic));
}
app.use('/', routes);
app.use('/imageview',imageview);

app.use(function(req, res, next) {
  var err = new Error('Not Found');
  err.status = 404;
  next(err);
});

if (app.get('env') === 'development') {
  app.use(function(err, req, res, next) {
    res.status(err.status || 500);
    res.render('error', {
      message: err.message,
      error: err
    });
  });
}

app.use(function(err, req, res, next) {
  res.status(err.status || 500);
  res.render('error', {
    message: err.message,
    error: {}
  });
});


module.exports = app;
